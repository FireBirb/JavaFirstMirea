package ru.mirea.task5;

public class SausageDoggo extends Dog{
    public SausageDoggo(String name, String color, int age) {
        this.name = name;
        this.color = color;
        this.fur_type = "short and glossy";
        this.age = age;
        this.breed = "sausage doggo";
        this.tailType = "regular stick";
        this.size = "medium";
    }
}
